public class TvACaboSwitchCase {
    public static void main(String[] args) {
        String plano = "Combo3";

        switch(plano){
            case "Combo3":{
                System.out.println("Celular");
            } 
            case "Combo2":{
                System.out.println("Internet");
            }
               
            case "Combo1":{
                System.out.println("TV a Cabo");
            }
        }  
       
    }
}
